public class Task3 {
    public static void main(String[] args) {
        System.out.println("contoh operator assignment");
        //operator assignment
        int a, b;
        a = 4;
        b = 3;
        //pengisian dan penambahan
        System.out.println(a);//hasilnya 4
        a+=b;
        //pengisian dan pengurangan
        System.out.println(a);//hasilnya 7
        a-=b;
        //pengisian dan perkalian
        System.out.println(a);//hasilnya 4
        a*=b;
        //pengisian dan pembagian
        System.out.println(a);//hasilnya 12
        a/=b;
        //pengisian dan sisa bagi
        System.out.println(a);//hasilnya 4
        a%=b;
        //hasil akhir pengisian
        System.out.println(a);//hasilnya 1
      
        
        
        
    }
}
